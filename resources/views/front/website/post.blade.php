              <p class="text font-italic">Otostopçunun Galaksi Rehberi‘ni</p><p>ilk olarak nereden duyduğumu bir türlü hatırlayamıyorum ama bir şekilde okumak istediğim kitaplar arasına girmişti. Douglas Adams‘a ait bu kitabın kült bir eser olduğunu biliyordum, seveninin çok olduğunu da. Kitaplarda ya da filmlerde de yine karşıma çıkıyordu. O halde okuma zamanı gelmişti artık.Bu beş ciltli kitabı çoğu yerde Alfa Yayınları’na ait versiyonunu bulabilirsiniz.

                  Otostopçunun Galaksi Rehberi, ilk başlarda gözümde bir gezi kitabı gibi canlanmıştı, nedense!

                  Arthur Dent, basit bir yaşam süren bir dünyalı. Ford Prefect ise yıllar önce dünyada mahsur kalmış, başka gezegenden bir otostopçu. Daha doğrusu, Otostopçunun Galaksi Rehberi adlı rehber kitap için bilgi toplarken yolu dünyaya düşmüş. Bir gün, dünyanın yok edileceği bilgisini alan Ford, arkadaşı Arthur’u bu saldırıdan son dakika kurtarır, ve kendilerini bir uzay gemisinde bulurlar. Böylece Arthur’a oldukça yabancı gelen bir evrendeki maceraları başlamış olur.</p>
            <p>Bu macera sırasında Arthur dünyanın asıl yaratılış nedenini öğrenecek, biz dünyalılar için önemli saydığımız birçok şeyin aslında önemsiz detaylar olduğunu, biz insanların birlikte yaşadığımız, hatta sevmediğimiz bazı canlılar için deney unsurları olduğumuzu öğrenecektir. Bu bilgiler Arthur’u oldukça şaşırtırken ilginç bir şekilde kendini bir anda evrenin nihai sorusunu ararken bulacaktır. İlginç bir şekilde diyorum, çünkü aslında cevabı belli olan ancak sorusu belli olmayan bu sorudan bahsediyoruz.

                Otostopçunun Galaksi Rehberi, itiraf ediyorum başlarda basit bir uzay romanı olarak gözüktü bana. Bildiğimiz, Star Wars- Star Trek gibi uzay filmleri veya romanlarından önce bu kitap olduğu için herhalde zamanında beğenilmiş diyordum. Ancak kitap beni yanılttı. Kitabı elimden bırakamadım. İçerdiği espriler, konunun size anlatılış biçimi, eski romanlarda gördüğümüz olaya müdahale ederek aslında karakterler böyle sanıyor ama olayın aslı bu şekilde şeklinde açıklamalarla kitap sizi sarıyor</p>

