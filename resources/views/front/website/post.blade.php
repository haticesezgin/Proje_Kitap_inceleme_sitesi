            <p>Otostopçunun Galaksi Rehberi, 5 kitaplık bir serinin ilk kitabı. Kitabın diğer seri kitaplardan da bir farkı var. Genelde her kitapta bir genel olay olur, çözümlenir, kitabın sonunda yeni bir problem çıkar ve diğer kitap buna odaklanmış olur. Ancak Bu kitapta baştan beri cevaplanmayan bir sürü sorular var, çözülmemiş olaylar var. Kitapta genel olarak şöyle bir olay oldu ve sonunda çözümlendi diyebileceğimiz bir durum yok. Olaylar ardı ardına gerçekleşiyor, belki de sorun 5.’in kitabın sonunda ancak çözülecek izlenimi verdi bana.

                Kısacası, Otostopçunun Galaksi Rehberi, Douglas Adams tarafından yazılan aslında bir seri. Benim gibi yanılıp sadece ilk kitabını okurum demeyin. Çünkü kesinlikle tamamlanmamış, giriş niteliğinde bir kitap. Bizi diğer kitaplarda çok fazla şeyin beklediğine eminim.

